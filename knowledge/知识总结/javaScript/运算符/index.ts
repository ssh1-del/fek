/*
运算符是一种数学符号，可以更加两个值（或者变量）产生出结果。
译注：这里说根据两个值产生结果不够严谨。计算两个变量的运算符称为'二元运算符'，还有一元运算符和三元运算符。
*/

//加法 将两个数字相加，或者两个字符串。 +
const number = 6 + 9;
const string = 'abc' + 'def';
//减法
const subtraction = 10 - 9;
//乘法
const multiplication = 2 * 10;
//除法
const division = 10 / 2;
//赋值运算
let a = '1';
//等于
{
    let b = 3;
    const c = b === 4;
}

//不等于 !==
{
    let a = 1;
    const c = a !== 2;
}
//取非 !  一元运算符
